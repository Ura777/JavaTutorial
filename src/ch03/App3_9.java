/*
 * 浮點數型態的最大值與最小值
 */

package ch03;

public class App3_9 
{

	public static void main(String[] args) 
	{
		double doubleMax = Double.MAX_VALUE;
		double doubleMin = Double.MIN_VALUE;
		float floatMax = Float.MAX_VALUE;
		float floatMin = Float.MIN_VALUE;
		
		System.out.println("The max value of double = " + doubleMax);
		System.out.println("The min value of double = " + doubleMin);
		System.out.println("The max value of float  = " + floatMax);
		System.out.println("The min value of float  = " + floatMin);

	}

}
