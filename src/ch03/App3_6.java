/*
 * 字元型態 char
 */

package ch03;

public class App3_6 
{

	public static void main(String[] args) 
	{
		//以ASCII碼來設定字元
		char ch1 = 71;
		char ch2 = 'G';
		//以16進位數值設定字元
		char ch3 = '\u0047';
		
		System.out.println("ch1 = " + ch1);
		System.out.println("ch2 = " + ch2);
		System.out.println("ch3 = " + ch3);

	}

}
