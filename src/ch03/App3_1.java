/*
 * 變數的使用
 */

package ch03;

public class App3_1 
{

	public static void main(String[] args) 
	{
		int num = 3;
		//宣告字元變數ch，值為'C'
		char ch = 'C';
		//宣告double常數PI，值為3.1415926
		final double PI = 3.1415926;
		
		System.out.println(num + " is an integer.");
		System.out.println(ch + " is a character.");
		System.out.println(PI + " is a constant.");

	}

}
