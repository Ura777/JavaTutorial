package ch03;

public class App3_12 
{

	public static void main(String[] args) 
	{
		int a = 25;
		int b = 9;
		
		System.out.println("a = " + a +", b = " + b);
		System.out.println("a/b = " + (a/b));
		System.out.println("(float)a/b = " + (float)a/b);

	}

}
